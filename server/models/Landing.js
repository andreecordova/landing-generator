const { Schema, model } = require("mongoose");

const LandingSchema = new Schema({
  prompt: {
    type: String,
    required: true,
  },
  content: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = model("Landing", LandingSchema);
